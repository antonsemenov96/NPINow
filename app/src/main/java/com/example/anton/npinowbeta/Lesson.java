class Lesson {
    String time;
    String room;
    String prepName;
    String discName;
    String discType;

    Lesson(String time, String room, String prepName, String discName, String discType) {
        this.time  = time;
        this.room = room;
        this.prepName= prepName;
        this.discName = discName;
        this.discType = discType;
    }

}
